package com.example.android_lab1;

public class FriendInfo {

        private String name;
        private boolean sender;

        public FriendInfo(String name, boolean sender) {
            this.name = name;
            this.sender = sender;
        }

        public String getName() {
            return name;
        }
        public boolean isSender() {
            return sender;
        }




}
